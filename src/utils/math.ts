/**
 * 加法运算
 * @group Math
 * @param a 第一个数
 * @param b 第二个数
 * @returns 两数之和
 */
export function add(a: number, b: number): number {
  return a + b;
}

/**
 * 减法运算
 * @group Math
 * @param a 第一个数
 * @param b 第二个数
 * @returns 两数之差
 */
export function subtract(a: number, b: number): number {
  return a - b;
}